    private void btnguimaxacnhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguimaxacnhanActionPerformed
        // TODO add your handling code here:
        if (txttaikhoanquenmk.getText().equals("")) {
            MessageConfig.messageText("không đươc để trống tài khoản", rootPane);
            return;
        }
        if (!RegexPattern.checkgmail(txttaikhoanquenmk.getText())) {
            MessageConfig.messageText("tài khoản không đúng định dạng", rootPane);
            return;
        }
        if (accountServic.selectFindOneServic(txttaikhoanquenmk.getText()) == null) {
            MessageConfig.messageText("tài khoản không có trong hệ thống", rootPane);
            return;
        }
        accountServic.guimaxacnhan(txttaikhoanquenmk.getText());
        lblgmail.setText(txttaikhoanquenmk.getText());
        showlayout("card4");
    }//GEN-LAST:event_btnguimaxacnhanActionPerformed

